package com.example.bankcards.entity;

/**
 * Перечисление возможных статусов банковской карты.
 * <p>
 * Определяет жизненный цикл карты в системе:
 * <ul>
 *   <li>{@link #CREATED} - карта создана, но еще не активирована</li>
 *   <li>{@link #ACTIVE} - карта активна и доступна для операций</li>
 *   <li>{@link #BLOCKED} - карта заблокирована (пользователем или администратором)</li>
 *   <li>{@link #EXPIRED_DATE} - срок действия карты истек</li>
 * </ul>
 * </p>
 *
 * @author Maxim Vorobev
 * @version 1.0
 * @see Card
 * @since 1.0
 */
public enum CardStatus {
    /**
     * Карта создана, но требует активации
     */
    CREATED,

    /**
     * Карта активна и готова к использованию
     */
    ACTIVE,

    /**
     * Карта заблокирована (временная или постоянная блокировка)
     */
    BLOCKED,

    /**
     * Срок действия карты истек
     */
    EXPIRED_DATE
}